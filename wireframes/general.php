        <p>Non-urgent patients are triaged to a separate area within the ED during evenings and weekends. Ambulance transport from another hospital and other referrals may be arranged by calling The Children's Hospital of Philadelphia's <a href="#">Emergency Transport Service</a> at <a href="tel://1-215-590-2160">215-590-2160</a> or <a href="tel://1-800-590-2160">1-800-590-2160</a>.</p>
