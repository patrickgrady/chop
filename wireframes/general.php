        <p class="deck-copy">Welcome to the Emergency Department (ED) at The Children's Hospital of Philadelphia. Our mission is to provide the best care for your child as quickly as possible. Specially trained nurses and doctors are available 24 hours a day to care for children with medical problems that cannot wait to be seen by their regular doctor. We know that it can be stressful to have a sick child and we will do everything we can to help families and their children during their emergency department visit.</p>
